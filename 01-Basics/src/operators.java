public class operators {
    public static void main(String[] args) {

        //Assignment operator(=)
        int num = 45;
        System.out.println(num);

        int num1 = num;
        System.out.println(num1);

        //Arithmetic Operators
        int a = 10;
        int b = 2;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

    }
}
